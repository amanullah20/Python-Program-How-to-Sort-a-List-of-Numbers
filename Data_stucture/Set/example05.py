# Example 05: Set intersection
# The intersection() method returns a new set containing commmon elements from both sets.

# You can also use the & operator to perform the intersection operation.

# Example sets
set1 = {1, 2, 3, 4}
set2 = {2, 3, 4, 5}

# Performing intersection
intersection_set = set1.intersection(set2)

# Printing the intersection set
print('Intersection of sets:', intersection_set)
