# Example 07: Checking Membership in a Dictionary 
# You can use 'in' keyword to check if a key exists in a dictionary.

# Example dictionary 
my_dict = {'name':'John','age':35}

# Checking for membership 
if 'name' in my_dict:
    print('key "name" exist in the dictionary ')
else: 
    print('key "name" does not exist in the dictionary')