# Example 06: Dictionary keys,values, and Items
# You can retrieve all keys,values , or key-value pairs using the keys(),values() and items() methods respectively.

# Example Dictionary 
my_dict = {'name':'Johan','age':24,'city':'Dhaka'}

# Retrieving keys values and items
keys= my_dict.keys()
values = my_dict.values()
items = my_dict.items()

# Printing keys,values,and items 
print('keys:',keys)
print('values:',values)
print('Items:',items)