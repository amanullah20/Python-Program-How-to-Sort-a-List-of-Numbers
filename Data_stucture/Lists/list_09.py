# Example 09
# Sorting a List
# The Sort() method sorts a list in ascending order.
# Yor can also sort in descending order by setting the reverse parameter to True.

# Example list
number_list = [5, 4, 3, 5, 6]
# sorting in ascending order
number_list.sort()
print('sorted list (Ascending ):', number_list)

# Sorting in Descending order
number_list.sort(reverse=True)

print('sorted List (Descending):', number_list)
